#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./scripts/setup-ssl.sh

set -e

echo "üîí –ù–∞—Å—Ç—Ä–æ–π–∫–∞ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞ –¥–ª—è profitech.store"

# 1. –°–æ–∑–¥–∞–µ–º –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –¥–ª—è ACME challenge
echo "üìÅ –°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –¥–ª—è ACME challenge..."
sudo mkdir -p /var/www/html/.well-known/acme-challenge
sudo chown -R www-data:www-data /var/www/html
sudo chmod -R 755 /var/www/html
echo "‚úÖ –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è —Å–æ–∑–¥–∞–Ω–∞"

# 2. –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é Nginx
echo ""
echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ Nginx..."
NGINX_CONF="/etc/nginx/sites-available/profitech"

if [ ! -f "$NGINX_CONF" ]; then
    echo "‚ùå –§–∞–π–ª $NGINX_CONF –Ω–µ –Ω–∞–π–¥–µ–Ω!"
    echo "   –°–æ–∑–¥–∞–π—Ç–µ –µ–≥–æ —Å–æ–≥–ª–∞—Å–Ω–æ –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏ –≤ docs/NGINX_CONFIG.md"
    exit 1
fi

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –±–ª–æ–∫ –¥–ª—è ACME challenge
if ! grep -q "\.well-known/acme-challenge" "$NGINX_CONF"; then
    echo "‚ö†Ô∏è  –ë–ª–æ–∫ –¥–ª—è ACME challenge –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏"
    echo "   –î–æ–±–∞–≤–ª—è—é –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏..."
    
    # –ü—ã—Ç–∞–µ–º—Å—è –¥–æ–±–∞–≤–∏—Ç—å –±–ª–æ–∫ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏
    if [ -f "./scripts/add-acme-to-nginx.sh" ]; then
        chmod +x ./scripts/add-acme-to-nginx.sh
        ./scripts/add-acme-to-nginx.sh
    else
        echo "   –°–∫—Ä–∏–ø—Ç add-acme-to-nginx.sh –Ω–µ –Ω–∞–π–¥–µ–Ω"
        echo "   –î–æ–±–∞–≤—å—Ç–µ —Å–ª–µ–¥—É—é—â–∏–π –±–ª–æ–∫ –ü–ï–†–ï–î location /:"
        echo ""
        echo "   location /.well-known/acme-challenge/ {"
        echo "       root /var/www/html;"
        echo "       try_files \$uri =404;"
        echo "   }"
        echo ""
        echo "   –û—Ç–∫—Ä–æ–π—Ç–µ —Ñ–∞–π–ª: sudo nano $NGINX_CONF"
        exit 1
    fi
else
    echo "‚úÖ –ë–ª–æ–∫ –¥–ª—è ACME challenge –Ω–∞–π–¥–µ–Ω"
fi

# 3. –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–∏–Ω—Ç–∞–∫—Å–∏—Å Nginx
echo ""
echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–∏–Ω—Ç–∞–∫—Å–∏—Å–∞ Nginx..."
if sudo nginx -t; then
    echo "‚úÖ –°–∏–Ω—Ç–∞–∫—Å–∏—Å –∫–æ—Ä—Ä–µ–∫—Ç–µ–Ω"
else
    echo "‚ùå –û—à–∏–±–∫–∞ –≤ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ Nginx!"
    exit 1
fi

# 4. –ü–µ—Ä–µ–∑–∞–≥—Ä—É–∂–∞–µ–º Nginx
echo ""
echo "üîÑ –ü–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∞ Nginx..."
sudo systemctl reload nginx
echo "‚úÖ Nginx –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∂–µ–Ω"

# 5. –ü—Ä–æ–≤–µ—Ä—è–µ–º –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç—å ACME challenge
echo ""
echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç–∏ ACME challenge..."
TEST_URL="http://profitech.store/.well-known/acme-challenge/test"
HTTP_CODE=$(curl -s -o /dev/null -w "%{http_code}" "$TEST_URL" || echo "000")

if [ "$HTTP_CODE" = "404" ]; then
    echo "‚úÖ ACME challenge –¥–æ—Å—Ç—É–ø–µ–Ω (404 - —ç—Ç–æ –Ω–æ—Ä–º–∞–ª—å–Ω–æ –¥–ª—è —Ç–µ—Å—Ç–æ–≤–æ–≥–æ —Ñ–∞–π–ª–∞)"
elif [ "$HTTP_CODE" = "000" ]; then
    echo "‚ö†Ô∏è  –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ –¥–æ–º–µ–Ω—É"
    echo "   –ü—Ä–æ–≤–µ—Ä—å—Ç–µ, —á—Ç–æ –¥–æ–º–µ–Ω –¥–æ—Å—Ç—É–ø–µ–Ω –∏–∑ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç–∞"
else
    echo "‚ÑπÔ∏è  HTTP –∫–æ–¥: $HTTP_CODE"
fi

# 6. –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ñ–∞–π—Ä–≤–æ–ª
echo ""
echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ñ–∞–π—Ä–≤–æ–ª–∞..."
if command -v ufw &> /dev/null; then
    UFW_STATUS=$(sudo ufw status | grep -c "80/tcp" || echo "0")
    if [ "$UFW_STATUS" = "0" ]; then
        echo "‚ö†Ô∏è  –ü–æ—Ä—Ç 80 –Ω–µ –æ—Ç–∫—Ä—ã—Ç –≤ —Ñ–∞–π—Ä–≤–æ–ª–µ"
        echo "   –û—Ç–∫—Ä—ã–≤–∞—é –ø–æ—Ä—Ç 80..."
        sudo ufw allow 80/tcp
        sudo ufw allow 443/tcp
        sudo ufw reload
        echo "‚úÖ –ü–æ—Ä—Ç—ã –æ—Ç–∫—Ä—ã—Ç—ã"
    else
        echo "‚úÖ –ü–æ—Ä—Ç 80 –æ—Ç–∫—Ä—ã—Ç"
    fi
else
    echo "‚ÑπÔ∏è  UFW –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω, –ø—Ä–æ–ø—É—Å–∫–∞–µ–º –ø—Ä–æ–≤–µ—Ä–∫—É —Ñ–∞–π—Ä–≤–æ–ª–∞"
fi

# 7. –ü–æ–ª—É—á–∞–µ–º —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç
echo ""
echo "üîí –ü–æ–ª—É—á–µ–Ω–∏–µ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞..."
echo "   –≠—Ç–æ –º–æ–∂–µ—Ç –∑–∞–Ω—è—Ç—å –Ω–µ—Å–∫–æ–ª—å–∫–æ –º–∏–Ω—É—Ç..."
echo ""

if sudo certbot --nginx -d profitech.store -d www.profitech.store --non-interactive --agree-tos --email admin@profitech.store; then
    echo ""
    echo "‚úÖ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç —É—Å–ø–µ—à–Ω–æ –ø–æ–ª—É—á–µ–Ω!"
    echo ""
    echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ HTTPS..."
    curl -I https://profitech.store 2>&1 | head -5 || echo "HTTPS –ø–æ–∫–∞ –Ω–µ –¥–æ—Å—Ç—É–ø–µ–Ω (–º–æ–∂–µ—Ç –ø–æ—Ç—Ä–µ–±–æ–≤–∞—Ç—å—Å—è –≤—Ä–µ–º—è)"
    echo ""
    echo "‚úÖ –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞!"
    echo ""
    echo "üìã –°–ª–µ–¥—É—é—â–∏–µ —à–∞–≥–∏:"
    echo "   1. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Å–∞–π—Ç: https://profitech.store"
    echo "   2. –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ —É–∂–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–æ"
    echo "   3. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ: sudo certbot renew --dry-run"
else
    echo ""
    echo "‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç"
    echo ""
    echo "üîç –î–∏–∞–≥–Ω–æ—Å—Ç–∏–∫–∞:"
    echo "   1. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ DNS: nslookup profitech.store"
    echo "   2. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç—å: curl -I http://profitech.store"
    echo "   3. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ª–æ–≥–∏: sudo tail -f /var/log/letsencrypt/letsencrypt.log"
    echo "   4. –°–º. –ø–æ–¥—Ä–æ–±–Ω—É—é –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—é: docs/SSL_TROUBLESHOOTING.md"
    exit 1
fi

