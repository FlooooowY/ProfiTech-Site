    server_name profitech.store www.profitech.store;

    # –í–ê–ñ–ù–û: –ë–ª–æ–∫ –¥–ª—è Let's Encrypt ACME challenge (–¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –ü–ï–†–ï–î location /)
